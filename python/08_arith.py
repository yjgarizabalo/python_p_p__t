# PEMDAS es un acrónimo de las palabras paréntesis, exponentes, multiplicación, división, suma, resta. Dadas al menos dos operaciones en una ecuación, el orden de las letras en PEMDAS te dice qué operación hacer primero, segundo, tercero, y así sucesivamente, hasta que la ecuación se resuelva